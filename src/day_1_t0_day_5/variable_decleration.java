package day_1_t0_day_5;

public class variable_decleration {

	public static void main(String[] args) {
		
		byte a= 10;
		short b= 20;
		int c= 30;
		long d= 40;
		float e= 20.3f;
		double f= 200.33;
		char ch= 'p';
		boolean h=true;
		System.out.println("variable decleration is:");
		System.out.println("the value of byte is :"+ a);
		System.out.println("the value of short is :"+ b);
		System.out.println("the value of int is :"+ c);
		System.out.println("the value of long is :"+ d);
		System.out.println("the value of float is :"+ e);
		System.out.println("the value of double is :"+ f);
		System.out.println("the value of char is :"+ ch);
		System.out.println("the value of boolean is :"+ h);
		

	}

}
