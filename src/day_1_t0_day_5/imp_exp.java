package day_1_t0_day_5;

public class imp_exp {
	public static void main(String[] args) {
		
		//implict conversion
		int a=20;
		double b=a;
		System.out.println("value of integer: "+ a);
		System.out.println("value of double: "+ b);
		
		//explict conversion
		float f= 20.3f;
		int c=(int)f;
		System.out.println("value of float: "+ f);
		System.out.println("value of integer: "+ c);


		

	}

}
