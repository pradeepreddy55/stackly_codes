package day_1_t0_day_5;

public class math_functions {
	public static void main(String[] args) {
		int a =25;
		int b=5;
		double c=25.05;
		double d=4.5;
		
		//maximum values
		System.out.println("maximum value is: "+Math.max(a, b));
		
		//minimum value
		System.out.println("minimum value is: "+Math.min(a, b));
		
		//square root
		System.out.println("square root value is: "+Math.sqrt(a));
		
		//power
		System.out.println("power value is: "+Math.pow(a,b));
		
		//absolute value
		System.out.println("absolute value is: "+Math.abs(-50));
		
		//round 
		System.out.println("round value is: "+Math.round(c));
		
		//floor value
		System.out.println("floor value is: "+Math.floor(c));
		
		//ceil value
		System.out.println("ceil value is: "+Math.ceil(c));
		
		//random value
		System.out.println("random value is: "+Math.random());
		
		//cube root
		System.out.println("cube root value is: "+Math.cbrt(b));
		
		//log value
		System.out.println("log value is: "+Math.log(a));
		
		//cos value
		System.out.println("cos value is: "+Math.cos(a));
		
		//sin value
		System.out.println("sin value is: "+Math.sin(b));

	}

}
